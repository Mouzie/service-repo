package za.co.reverside.service;

public class Window{

	private String color;

	private double size;

	public String getColor(){
		return this.color;
	}

	public void setColor(String color){
		this.color = color;
	}

	public double getSize(){
		return this.size;
	}

	public void setSize(double size){
		this.size = size;
	}
}